// box1
function initECharts(data, container) {
  var myChart = echarts.init(container);
  var option = {
    tooltip: {
      show: true,
      trigger: "item",
      formatter: "{b}: {c} 人",
    },

    series: [
      {
        type: "sunburst",
        center: ["50%", "50%"],
        radius: ["0%", "100%"],
        sort: "null",
        data: data,
        label: {
          rotate: "tangential",
          color: "#fff",
          minAngle: 30,
        },
        itemStyle: {
          color: "#eee",
          borderColor: "#fff",
          borderWidth: 5,
          borderRadius: 20,
        },
        levels: [
          {
            emphasis: {
              focus: "descendant",
            },
          },
          {
            r0: "30%",
            r: "60%",
            emphasis: {
              focus: "descendant",
            },
            itemStyle: {
              borderWidth: 2,
              borderColor: "#fff",
              borderRadius: 8, // 设置圆角半径为 8px
            },
          },
          {
            r0: "60%",
            r: "100%",
            emphasis: {
              focus: "self",
            },
            itemStyle: {
              borderWidth: 2,
              borderColor: "#fff",
              borderRadius: 8, // 设置圆角半径为 8px
            },
          },
        ],
      },
    ],
  };

  myChart.setOption(option);

  window.addEventListener("resize", function () {
    myChart.resize();
  });

  return myChart;
}
